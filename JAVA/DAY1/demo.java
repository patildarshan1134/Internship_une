import java.util.*;

public class Split {
	
		    public static void main(String[] args)
		    {
		        Scanner sc=new Scanner(System.in);
		        int a=sc.nextInt();
		        
		    	System.out.println("Left Shift operation:"+(a<<2));
		    	System.out.println("Right Shift operation:"+(a>>2));
		    	
		    	System.out.println("unsigned Right Shift operation:"+(a>>>2));
		    }
		
	}