

public class DemoExplicit {

	public static void main(String[] args) {
		double num1=3534.4545;
        int num2=(int)num1;         //explicit type casting
        System.out.println("Double Value:-"+num1);
        System.out.println("integer Value:-"+num2);
        
        
        int num3=35;
        short num4=(short)num3;      //explicit type casting
        System.out.println("integer Value:-"+num3);
        System.out.println("short Value:-"+num4);
        
		

	}

}
